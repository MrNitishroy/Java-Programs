public class O7_Swap_two_number_with_temp{
    public static void main(String[] args) {
        int a=10;
        int b=30;
        int temp;
        System.out.println("Befor swaping value is A = " +a+ " B = " +b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("After swaping value is A = " +a+ " B = " +b);

    }
}